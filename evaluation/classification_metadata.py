def build_classifier_metadata_text(machine_id, window_size, horizon, best_params,
                                   best_threshold, val_metrics):
    return (
        f"machine_id: {machine_id}\n"
        f"model_name: hist_gb_classifier\n"
        f"window_size: {window_size}\n"
        f"horizon: {horizon}\n"
        f"\n"
        f"fixed_hyperparameters:\n"
        f"  loss=log_loss\n"
        f"  early_stopping=False\n"
        f"  random_state=42\n"
        f"\n"
        f"best_tuned_hyperparameters:\n"
        f"  learning_rate={best_params['learning_rate']}\n"
        f"  max_iter={best_params['max_iter']}\n"
        f"  max_leaf_nodes={best_params['max_leaf_nodes']}\n"
        f"  min_samples_leaf={best_params['min_samples_leaf']}\n"
        f"\n"
        f"threshold_selection:\n"
        f"  selected_on=validation\n"
        f"  rule=best_f1_with_margin_then_recall_then_precision\n"
        f"  best_threshold={best_threshold}\n"
        f"\n"
        f"model_selection:\n"
        f"  rule=max_pr_auc_with_margin_then_min_brier_then_max_f1_then_recall_then_precision\n"
        f"\n"
        f"search_strategy:\n"
        f"  type=staged_grid_search\n"
        f"  stage_1=max_iter_100_for_all_branches\n"
        f"  stage_2=refine_top_branches_with_higher_max_iter\n"
        f"  branch_stop_rule=stop_if_metrics_clearly_deteriorate_when_max_iter_increases\n"
        f"\n"
        f"best_validation_metrics:\n"
        f"  positive_rate={val_metrics['positive_rate']}\n"
        f"  pr_auc={val_metrics['pr_auc']}\n"
        f"  brier_score={val_metrics['brier_score']}\n"
        f"  false_alert_rate={val_metrics['false_alert_rate']}\n"
        f"  precision_at_threshold={val_metrics['precision_at_threshold']}\n"
        f"  recall_at_threshold={val_metrics['recall_at_threshold']}\n"
        f"  f1_at_threshold={val_metrics['f1_at_threshold']}\n"
    )


